
public class ClothesInfo {
    private String size;
    private String color;
    private double price;

    public ClothesInfo(String size, String color, double price) {
        this.size = size;
        this.color = color;
        this.price = price;
    }

    public String getSize() {
        return size;
    }

    public String getColor() {
        return color;
    }

    public double getPrice() {
        return price;
    }

}
